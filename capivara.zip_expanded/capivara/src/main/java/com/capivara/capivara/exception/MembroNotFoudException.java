package com.capivara.capivara.exception;

public class MembroNotFoudException extends Exception {

	private static final long serialVersionUID = 7253786053965296017L;

	public MembroNotFoudException() {
		super();
		
	}

	public MembroNotFoudException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public MembroNotFoudException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public MembroNotFoudException(String message) {
		super(message);
		
	}

	public MembroNotFoudException(Throwable cause) {
		super(cause);
		

}
}